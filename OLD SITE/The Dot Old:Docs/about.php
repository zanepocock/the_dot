<p> Welcome to The Dot</p>
<p>&nbsp;</p>
<p>Here is a website for artists, curators, gallery directors, gallery goers, writers, dealers and everyone in between to establish relationships and enable careers. </p>
<p>&nbsp;</p>
<p>Through our forum you can discuss, learn and connect the dots. By clicking on the other tabs, you can gratify your curiosity by keeping up with emerging artists, cool studio spaces and the artists that have made it big in New Zealand.</p>
<p>&nbsp;</p>
<p>The Dot aims to be a helping tool for people in a career in the visual arts that is often rocky and lonesome. But The Dot also hopes to efficiently categorize New Zealand galleries in an accessible way that hasn't been done before.</p>
<p>&nbsp;</p>
