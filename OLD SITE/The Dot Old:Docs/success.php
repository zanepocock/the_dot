<p> "I have had a life-long love of photography, though I did not go to art school and have followed my own haphazard pathway.</p>
 <p>&nbsp; </p>

<p>I am very interested in recording my daily life and my surroundings and try to carry a camera with me at all times. I have maintained a daily photo blog for the last four years; I take a number of photos each day, and choose one to upload to my photo blog.  This self-imposed need to produce a photo each and every day, has been a good discipline for me and definitely sharpened my awareness. </p>
 <p>&nbsp; </p>

<p>James Gilberd from Photospace in Wellington has always been very supportive and I have had a couple of exhibitions at Photospace. He also supported my submission to the Wellington City Council City Arts to exhibit my images in the Courtenay Place lightboxes in 2009.</p>
 <p>&nbsp; </p>

<p>The best advice I was given (and I wish I had received it years ago!) was: "take thousands of photos".</p>
